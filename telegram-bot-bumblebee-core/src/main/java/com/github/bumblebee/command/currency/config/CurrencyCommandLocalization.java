package com.github.bumblebee.command.currency.config;

public class CurrencyCommandLocalization {
    private String chartTitle;
    private String chartDateAxis;
    private String chartRateAxis;

    public String getChartTitle() {
        return chartTitle;
    }

    public void setChartTitle(String chartTitle) {
        this.chartTitle = chartTitle;
    }

    public String getChartDateAxis() {
        return chartDateAxis;
    }

    public void setChartDateAxis(String chartDateAxis) {
        this.chartDateAxis = chartDateAxis;
    }

    public String getChartRateAxis() {
        return chartRateAxis;
    }

    public void setChartRateAxis(String chartRateAxis) {
        this.chartRateAxis = chartRateAxis;
    }
}
